export type Language = (typeof AVAILABLE_LANGUAGES)[number]['code'];

export const LANGUAGE_CODES = AVAILABLE_LANGUAGES.map((language) => language.code);

export function isSupportedLanguage(language: string | null | undefined): language is Language {
  return Boolean(language && LANGUAGE_CODES.includes(language as Language));
}
